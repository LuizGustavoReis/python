


n = input('digite algo')

print('o tipo primitivo é ',type(n))
print('so tem espaços?',n.isspace())
print('e um numero?',n.isnumeric())
print('e alfabetico?',n.isalpha())
print('ele e alfabetico e numerico?',n.isalnum())
print('esta em maiusculas?',n.isupper())
print('esta em minuscular',n.islower())
print('esta capitalizado?',n.istitle())

print('sua resposta e do tipo {} '.format(type(n)))